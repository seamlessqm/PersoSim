   protected void addCaKeys(MasterFile mf) throws AccessDeniedException {
      this.initPersonalizationDataContainer();
      ArrayList<KeyPair> caKeys = this.persoDataContainer.getCaKeys();
      ArrayList<Integer> caKeyIds = this.persoDataContainer.getCaKeyIds();
      ArrayList<Boolean> caKeyPriv = this.persoDataContainer.getCaKeyPrivileges();

      for (int i = 0; i < caKeys.size(); i++) {
         KeyPairObject caKey = new KeyPairObject(caKeys.get(i), new KeyIdentifier(caKeyIds.get(i)), caKeyPriv.get(i));
         caKey.addOidIdentifier(Ca.OID_IDENTIFIER_id_CA_ECDH_AES_CBC_CMAC_128);
         mf.addChild(caKey);
      }
   }

